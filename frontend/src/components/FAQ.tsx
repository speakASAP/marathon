import { useState } from 'react';

const FAQ_ITEMS: { q: string; a: string }[] = [
  {
    q: 'Как организован марафон?',
    a: 'На прохождение марафона дается 30 дней + 7 дополнительных. Марафон состоит из этапов. В конце каждого дня вы публикуете отчет. Все вопросы вы задаете остальным марафонцам или кураторам.',
  },
  {
    q: 'Когда я получу очередное задание?',
    a: 'Каждый участник выставляет время, до которого ему удобно выполнить задание и написать отчет. Новое задание появляется после публикации отчета текущего этапа в указанное время.',
  },
  {
    q: 'Что делать, если я не успел(а) опубликовать отчет?',
    a: 'До 3 этапа за опоздания не штрафуетесь. С 3 этапа есть возможности «Штрафной круг» или «Списать день». «Штрафной круг» дается автоматически в первый раз.',
  },
  {
    q: 'Что будет, если я истрачу все бонусные дни?',
    a: 'Если использовали штрафной круг и 7 дополнительных дней, вы сходите с дистанции. Доступ к пройденным этапам остается.',
  },
];

export default function FAQ() {
  const [open, setOpen] = useState<number | null>(null);

  return (
    <section className="landing-section" id="faq">
      <h2>Ответы на частые вопросы</h2>
      <div className="faq-list">
        {FAQ_ITEMS.map((item, i) => (
          <div key={i} className="faq-item">
            <button
              type="button"
              className="faq-question"
              onClick={() => setOpen(open === i ? null : i)}
              aria-expanded={open === i}
            >
              {item.q}
            </button>
            {open === i && <div className="faq-answer">{item.a}</div>}
          </div>
        ))}
      </div>
    </section>
  );
}
